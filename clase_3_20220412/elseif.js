let edad = 7;

if (edad >= 1 && edad <= 6) {
    console.log("Es un bebe")
}
else if (edad >= 7 && edad <= 15) {
    console.log("Es un pre-adolecente")
}
else {
    console.log("No hubo coincidencia")
}